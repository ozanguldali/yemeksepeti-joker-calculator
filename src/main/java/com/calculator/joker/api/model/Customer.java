package com.calculator.joker.api.model;

public class Customer {

    private String name;

    private String cost;

    Customer() {

    }

    public String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    public String getCost() {
        return cost;
    }

    void setCost(String cost) {
        this.cost = cost;
    }

}
